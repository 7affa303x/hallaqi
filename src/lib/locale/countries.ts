export function countryLabel(country: CountryOption, language: 'ar' | 'fr' | 'en'): string {
  if (language === 'fr') return country.nameFr;
  if (language === 'en') return country.nameEn;
  return country.nameAr;
}

export function findCountry(code?: string | null): CountryOption | undefined {
  if (!code) return undefined;
  return WORLD_COUNTRIES.find(c => c.code === code.toUpperCase());
}

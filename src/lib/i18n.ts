export function translate(language: SupportedLanguage, key: TranslationKey): string {
  const pack = messages[language] || messages.ar;
  return pack[key] || messages.ar[key] || String(key);
}

export function isRtlLanguage(language: SupportedLanguage): boolean {
  return language === 'ar';
}

export function languageLabel(language: SupportedLanguage): string {
  if (language === 'fr') return 'Français';
  if (language === 'en') return 'English';
  return 'العربية';
}
